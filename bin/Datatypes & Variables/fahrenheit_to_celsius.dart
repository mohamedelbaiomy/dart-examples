// Write a program that converts from Fahrenheit to Celsius
//
// * declare a double variable to store the degree in Fahrenheit
// * declare a double variable to store the degree in Celsius
//
// Hint : celsius = 5/9 * (fahrenheit-32)

void main() {
  double degreeInFahrenheit = 314.7;
  double degreeInCelsius = 5 / 9 * (degreeInFahrenheit - 32);
  print(degreeInCelsius);
}
