// Declare three variables called rating1, rating2 and rating3 of type double
// and assign each a value (any value). Calculate the average of the three and
// store the result in a variable named averageRating and print it.
//
// الـ average اللى هو المتوسط وده بيساوى مجموع الثلاث متغيرات / عدد المتغيرات
// زى الـ mean بالظبط

void main() {
  double rating1 = 8.5;
  double rating2 = 7.8;
  double rating3 = 9.2;

  double averageRating = (rating1 + rating2 + rating3) / 3;

  print("The average rating is: $averageRating");
}
